fn main() {
    let x = 2.0; // f64
    let y: f32 = 3.0; // f32
    println!("x is: {x}");
    println!("y is: {y}");
    // numeric operations
    // addition
    let sum = 5 + 10;

    // substraction
    let difference = 95.5 - 4.3;

    // multiplication
    let product = 4 * 30;

    // division
    let quotient = 56.7 / 32.2;
    let floored = 2 / 3;

    // remainder
    let remainder = 43 % 5;
    println!("sum: {sum}");
    println!("difference: {difference}");
    println!("product: {product}");
    println!("quotient: {quotient}");
    println!("floored: {floored}");
    println!("remainder: {remainder}");
}
